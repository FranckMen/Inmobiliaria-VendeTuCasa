
package src.cuentas;


public class Cuentas {
    private int id;
private String nombre, apellido_paterno, apellido_materno, email, contrasena, calle, municipio, estado, cp, telefono;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getNombre() {
	return nombre;
}
public void setNombre(String nombre) {
	this.nombre = nombre;
}
public String getApellidoPaterno() {
	return apellido_paterno;
}
public void setApellidoPaterno(String apellido_paterno) {
	this.apellido_paterno = apellido_paterno;
}
public String getApellidoMaterno() {
	return apellido_materno;
}
public void setApellidoMaterno(String apellido_materno) {
	this.apellido_materno = apellido_materno;
}
public String getContrasena() {
	return contrasena;
}
public void setContrasena(String contrasena) {
	this.contrasena = contrasena;
}
public String getEmail() {
	return email;
}
public void setEmail(String email) {
	this.email = email;
}
public String getCalle() {
	return calle;
}
public void setCalle(String calle) {
	this.calle = calle;
}
public String getMunicipio() {
	return municipio;
}
public void setMunicipio(String municipio) {
	this.municipio = municipio;
}
public String getEstado() {
	return estado;
}
public void setEstado(String estado) {
	this.estado = estado;
}
public String getCp() {
	return cp;
}
public void setCp(String cp) {
	this.cp = cp;
}
public String getTelefono() {
	return telefono;
}
public void setTelefono(String telefono) {
	this.telefono = telefono;
}
}
